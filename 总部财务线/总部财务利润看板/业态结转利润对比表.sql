    --         CASE WHEN SUM(签约不含税_25年签约) = 0 THEN 0 ELSE SUM(净利润_25年签约) / SUM(签约不含税_25年签约) END
    --             - CASE WHEN SUM(签约不含税_25年预算) = 0 THEN 0 ELSE SUM(净利润_25年预算) / SUM(签约不含税_25年预算) END AS 项目实际较预算偏差,
            
